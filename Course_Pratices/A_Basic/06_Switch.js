let a = 3;

switch (a) {
    case 0:
        console.log("zero");
        break;
    case 1:
        console.log("ONE");
        break;
    case 2:
        console.log("two");
        break;
    case 3:
        console.log("three");
        break;
    default:
        console.log("four");
        break;
}